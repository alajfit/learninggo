package main

import "fmt"

func main() {

	/*	Varibale Declaration:
		Variables can be declared using var
		A type can be and should be defined
		Multiple are available
	*/
	var age int = 30
	var favNum float64 = 1.6180339
	/*
		Variables can also be auto asigned a type
		Once a type is auto assigned it can not be changed
	*/
	radNum := 1

	// This is how multi line prints are done
	fmt.Println(age, favNum, radNum)
}
